#!/usr/bin/env python3
"""Aggregate every run under results/ into a single markdown table.

Reads summary.json + programs.sqlite from each run directory and emits
results/RESULTS.md. Re-run any time; idempotent.

Layout expected:
    results/llm/<problem>/{summary.json,programs.sqlite,events.jsonl}
    results/baselines/<problem>-random/{summary.json,programs.sqlite,events.jsonl}
"""
from __future__ import annotations

import json
import sqlite3
import sys
from pathlib import Path
from typing import Optional


ROOT = Path(__file__).resolve().parents[1]
RES = ROOT / "results"
OUT = RES / "RESULTS.md"


def _summary(d: Path) -> Optional[dict]:
    p = d / "summary.json"
    if not p.exists():
        return None
    try:
        return json.loads(p.read_text())
    except Exception:
        return None


def _stats(d: Path) -> dict:
    """Pull counts + best from the sqlite db. Tolerates partial runs."""
    db = d / "programs.sqlite"
    out: dict = {"programs": None, "ok_programs": None, "max_gen": None,
                 "best_score": None, "llm_calls": None}
    if not db.exists():
        return out
    try:
        c = sqlite3.connect(f"file:{db}?mode=ro", uri=True)
        c.row_factory = sqlite3.Row
        out["programs"] = c.execute("SELECT COUNT(*) AS c FROM programs").fetchone()["c"]
        out["ok_programs"] = c.execute(
            "SELECT COUNT(*) AS c FROM programs WHERE status='ok'"
        ).fetchone()["c"]
        out["max_gen"] = c.execute(
            "SELECT COALESCE(MAX(generation),0) AS g FROM programs"
        ).fetchone()["g"]
        # llm_calls table may not exist for --no-llm runs
        try:
            out["llm_calls"] = c.execute(
                "SELECT COUNT(*) AS c FROM llm_calls"
            ).fetchone()["c"]
        except sqlite3.OperationalError:
            out["llm_calls"] = 0
        c.close()
    except Exception as e:
        out["error"] = str(e)
    return out


def _row(label: str, run_dir: Path) -> str:
    s = _summary(run_dir) or {}
    st = _stats(run_dir)
    baseline = s.get("baseline_score")
    best = s.get("best_score") if s.get("best_score") is not None else "—"
    if isinstance(best, float):
        best_s = f"{best:g}"
    else:
        best_s = str(best)
    delta = "—"
    if isinstance(s.get("best_score"), (int, float)) and isinstance(baseline, (int, float)):
        d = s["best_score"] - baseline
        delta = f"{d:+g}"
    progs = st.get("programs") or "—"
    gen = st.get("max_gen") or "—"
    llm = st.get("llm_calls") or 0
    state = "✅ done" if "best_score" in s else "⏳ running"
    return (
        f"| {label} | {best_s} | {baseline if baseline is not None else '—'} | "
        f"{delta} | {gen} | {progs} | {llm} | {state} |"
    )


def main() -> int:
    if not RES.exists():
        print(f"[aggregate] no results directory at {RES}", file=sys.stderr)
        return 1

    problems = ["sortnet8", "matmul44_z2", "capset-6"]

    lines = []
    lines.append("# zero — benchmark results")
    lines.append("")
    lines.append("Auto-generated by `scripts/aggregate_results.py`. Re-run any time.")
    lines.append("")
    lines.append("Lower-is-better: `sortnet8`, `matmul44_z2`. Higher-is-better: `capset-6`.")
    lines.append("")
    lines.append("## Headline table")
    lines.append("")
    lines.append("| Run | Best | Baseline | Δ | Gen | Programs | LLM calls | State |")
    lines.append("|-----|------|----------|---|-----|----------|-----------|-------|")
    for prob in problems:
        llm_dir = RES / "llm" / prob
        base_dir = RES / "baselines" / f"{prob}-random"
        if llm_dir.exists():
            lines.append(_row(f"`{prob}` LLM (Claude Opus)", llm_dir))
        if base_dir.exists():
            lines.append(_row(f"`{prob}` random-only", base_dir))
    lines.append("")
    lines.append("## Per-run details")
    lines.append("")
    for prob in problems:
        for label, sub in [("LLM (Claude Opus)", RES / "llm" / prob),
                            ("Random-only baseline", RES / "baselines" / f"{prob}-random")]:
            if not sub.exists():
                continue
            s = _summary(sub) or {}
            st = _stats(sub)
            lines.append(f"### `{prob}` — {label}")
            lines.append("")
            if s:
                lines.append("```json")
                lines.append(json.dumps(s, indent=2))
                lines.append("```")
            else:
                lines.append("_(run still in progress, no summary yet)_")
            lines.append("")
            lines.append(f"- ok programs: **{st.get('ok_programs') or 0}** / "
                         f"{st.get('programs') or 0}")
            lines.append(f"- max generation reached: **{st.get('max_gen') or 0}**")
            lines.append(f"- llm calls logged: **{st.get('llm_calls') or 0}**")
            lines.append("")
    OUT.write_text("\n".join(lines) + "\n")
    print(f"[aggregate] wrote {OUT}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
